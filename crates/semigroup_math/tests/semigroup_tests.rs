    // fast_descent (when f ≥ 2m) collapses k = #(apéry ∈ (f, f+m)) per-step
    // !is_descent removals (each Δr = −2) followed by one is_descent step
    // (Δr = m − 2). Net: Δr = (m − 2) − 2·k.
