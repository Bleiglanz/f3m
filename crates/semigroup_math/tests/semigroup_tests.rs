#[test]
fn test_ord_containment() {
    // <6,9,20> ⊂ <6,9,20,43> (adding the Frobenius number as a generator)
    let s1 = compute(&[6, 9, 20]);
    let s2 = compute(&[6, 9, 20, 43]);
    assert!(s1 < s2);
    assert!(s2 > s1);

    // Equal semigroups
    let s3 = compute(&[6, 9, 20]);
    assert_eq!(s1.partial_cmp(&s3), Some(std::cmp::Ordering::Equal));

    // <3,7> and <2,7> are incomparable: 2 ∉ <3,7> and 3 ∉ <2,7>
    let a = compute(&[3, 7]);
    let b = compute(&[2, 7]);
    assert_eq!(a.partial_cmp(&b), None);
}

/// Brute-force enumeration of every level-1 numerical semigroup with given
/// multiplicity `m` (i.e. m < f < 2m). For each μ ∈ {1, …, m−1} and each
/// subset of {1, …, μ−1} (telling us which `m+r` are in S), build the
/// candidate Apéry set, run `compute`, and yield the result. Every candidate
/// gives a valid numerical semigroup (the Kunz constraints are automatic at
/// level 1, since w_r + w_s ≥ 2m + r + s dominates every right-hand side).
fn level_1_semigroups_for_m(m: usize) -> Vec<(usize, semigroup_math::math::Semigroup)> {
    let mut out = Vec::new();
    for mu in 1..m {
        // Free choices: r ∈ {1, …, μ−1}. For r > μ, w_r = m+r is forced.
        let free = mu - 1;
        for mask in 0..(1u32 << free) {
            let mut gens = vec![m];
            for r in 1..m {
                let w = if r == mu {
                    2 * m + mu
                } else if r > mu || (mask >> (r - 1)) & 1 == 1 {
                    // r > mu: forced m+r; r < mu and bit set: m+r ∈ S.
                    m + r
                } else {
                    2 * m + r
                };
                gens.push(w);
            }
            out.push((mu, compute(&gens)));
        }
    }
    out
}

#[test]
fn test_level_0_uniqueness() {
    // For every multiplicity m ≥ 2 there is exactly one level-0 numerical
    // semigroup: the ordinary semigroup S = {0} ∪ [m, ∞), generated by
    // {m, m+1, …, 2m−1}. Its invariants are f = g = μ = m − 1.
    for m in 2..=12 {
        let gens: Vec<usize> = (m..2 * m).collect();
        let s = compute(&gens);
        assert_eq!(s.m, m, "m={m}");
        assert_eq!(s.level, 0, "level should be 0 for m={m}");
        assert_eq!(s.f, m - 1, "f should be m−1 for m={m}");
        assert_eq!(s.mu, m - 1, "μ should be m−1 for m={m}");
        assert_eq!(s.g, m - 1, "g should be m−1 for m={m}");
        assert_eq!(s.gen_set, gens, "min-gens should be {{m,…,2m−1}} for m={m}");
    }
}

/// Bit-encoded enumeration helper: subset of `{1, …, μ−1}` from a mask where
/// bit `r−1` set means `r` is included.
fn mask_to_subset(mask: u32, mu: usize) -> Vec<usize> {
    (1..mu).filter(|&r| (mask >> (r - 1)) & 1 == 1).collect()
}

/// `M` is sum-avoiding for parameter `μ`: no `a, b ∈ M` (allowing `a = b`)
/// satisfy `a + b = μ`. Equivalently, `M ∩ (μ − M) = ∅`, with the additional
/// requirement that `μ/2 ∉ M` when `μ` is even.
fn is_sum_avoiding(m_set: &[usize], mu: usize) -> bool {
    for &a in m_set {
        for &b in m_set {
            if a + b == mu {
                return false;
            }
        }
    }
    true
}

/// Builds the candidate level-2 "missing" semigroup whose Apéry set is
/// `(0, …, w_r = m+r if r ∈ M, 2m+r otherwise; w_μ = 3m+μ; m+r forced for r > μ)`.
/// Returns the result of `compute` on those Apéry elements as generators
/// (plus `m`). The resulting semigroup is level 2 with the intended `(m, μ)`
/// **iff** `M` is sum-avoiding for `μ`; otherwise `2m+μ` becomes reachable
/// and the result collapses back to level 1.
fn build_level_2_missing(m: usize, mu: usize, m_set: &[usize]) -> semigroup_math::math::Semigroup {
    let mut gens = vec![m];
    for r in 1..m {
        let w = if r == mu {
            3 * m + mu
        } else if r > mu || m_set.contains(&r) {
            m + r
        } else {
            2 * m + r
        };
        gens.push(w);
    }
    compute(&gens)
}

/// Same as [`build_level_2_missing`] but with `w_μ = 2m+μ`, producing the
/// level-1 semigroup at the same `M`. Always valid (level 1 has no Kunz
/// filtering).
fn build_level_1(m: usize, mu: usize, m_set: &[usize]) -> semigroup_math::math::Semigroup {
    let mut gens = vec![m];
    for r in 1..m {
        let w = if r == mu {
            2 * m + mu
        } else if r > mu || m_set.contains(&r) {
            m + r
        } else {
            2 * m + r
        };
        gens.push(w);
    }
    compute(&gens)
}

#[test]
fn test_level_2_missing_stratification() {
    // For every (m, μ) with m ≤ 8, verify:
    //   • Per-stratum count by k = |M|:
    //       level-1 stratum has C(μ−1, k) subsets,
    //       level-2 missing stratum has C(⌊(μ−1)/2⌋, k) · 2^k subsets,
    //   • The lift M ↦ M takes a level-1 with sum-avoiding M to a level-2
    //     "missing" with the same M, raising genus by exactly 1.
    //   • Non-sum-avoiding M's "lift" to a level-1 (collapse), not a
    //     level-2.
    for m in 2..=8 {
        for mu in 1..m {
            let mut l1_by_k: Vec<usize> = vec![0; mu];
            let mut l2_missing_by_k: Vec<usize> = vec![0; mu];
            for mask in 0..(1u32 << (mu - 1)) {
                let m_set = mask_to_subset(mask, mu);
                let k = m_set.len();
                l1_by_k[k] += 1;

                let s1 = build_level_1(m, mu, &m_set);
                assert_eq!(s1.m, m, "(m={m}, μ={mu}, M={m_set:?})");
                assert_eq!(s1.mu, mu, "(m={m}, μ={mu}, M={m_set:?})");
                assert_eq!(s1.level, 1, "(m={m}, μ={mu}, M={m_set:?})");
                assert_eq!(s1.g, m + mu - 1 - k, "level-1 genus formula");

                let s2 = build_level_2_missing(m, mu, &m_set);
                if is_sum_avoiding(&m_set, mu) {
                    l2_missing_by_k[k] += 1;
                    assert_eq!(s2.m, m, "(m={m}, μ={mu}, M={m_set:?})");
                    assert_eq!(s2.mu, mu, "(m={m}, μ={mu}, M={m_set:?})");
                    assert_eq!(s2.level, 2, "(m={m}, μ={mu}, M={m_set:?})");
                    assert_eq!(s2.g, m + mu - k, "missing genus formula");
                    assert_eq!(s2.g, s1.g + 1, "genus shift l=1 → l=2 missing must be +1",);
                } else {
                    // Bad pair in M: 2m+μ reduces, w_μ collapses to 2m+μ,
                    // and the result lands back in level 1 (not level 2).
                    assert_ne!(
                        s2.level, 2,
                        "non-sum-avoiding M must not lift to level 2: m={m}, μ={mu}, M={m_set:?}",
                    );
                }
            }

            // Closed forms.
            let half = (mu - 1) / 2;
            for k in 0..mu {
                assert_eq!(
                    l1_by_k[k],
                    binom(mu - 1, k).expect("small"),
                    "level-1 stratum (m={m}, μ={mu}, k={k})",
                );
                let predicted_missing = if k <= half {
                    binom(half, k).expect("small") * (1usize << k)
                } else {
                    0
                };
                assert_eq!(
                    l2_missing_by_k[k], predicted_missing,
                    "level-2 missing stratum (m={m}, μ={mu}, k={k})",
                );
            }

            // Sum totals: level-1 = 2^(μ−1), level-2 missing = 3^⌊(μ−1)/2⌋.
            assert_eq!(l1_by_k.iter().sum::<usize>(), 1usize << (mu - 1));
            assert_eq!(
                l2_missing_by_k.iter().sum::<usize>(),
                3usize.pow(half as u32)
            );
        }
    }
}

#[test]
fn test_level_1_count_formula() {
    // N(g, l=1, m, μ) = C(μ−1, m + μ − 1 − g) for g ∈ {m, …, m+μ−1};
    // zero outside that range. Verified by brute force.
    for m in 2..=8 {
        let mut actual: std::collections::HashMap<(usize, usize), usize> =
            std::collections::HashMap::new();
        for (mu, s) in level_1_semigroups_for_m(m) {
            assert_eq!(s.m, m, "m mismatch (m={m}, μ={mu})");
            assert_eq!(s.level, 1, "level mismatch (m={m}, μ={mu})");
            assert_eq!(s.mu, mu, "μ mismatch (m={m})");
            *actual.entry((s.g, mu)).or_insert(0) += 1;
        }
        // Compare against the closed form for every (g, μ) we observed.
        for ((g, mu), &n) in &actual {
            let k = m + mu - 1 - g;
            let predicted = if k < *mu {
                binom(mu - 1, k).expect("small inputs cannot overflow")
            } else {
                0
            };
            assert_eq!(
                n, predicted,
                "N(g={g}, l=1, m={m}, μ={mu}): predicted {predicted}, actual {n}",
            );
        }
        // And verify the sum: N(g, l=1, m) = Σ_μ N(g, l=1, m, μ).
        let mut by_g: std::collections::HashMap<usize, usize> = std::collections::HashMap::new();
        for ((g, _), n) in &actual {
            *by_g.entry(*g).or_insert(0) += n;
        }
        for (&g, &actual_total) in &by_g {
            let predicted_total: usize = (1..m)
                .map(|mu| {
                    if g >= m && g < m + mu {
                        binom(mu - 1, m + mu - 1 - g).expect("small")
                    } else {
                        0
                    }
                })
                .sum();
            assert_eq!(
                actual_total, predicted_total,
                "N(g={g}, l=1, m={m}): predicted {predicted_total}, actual {actual_total}",
            );
        }
    }
}

/// Compute the semigroup from `gens` and assert every GAP-verified property.
///
/// Parameters match the GAP assertions in gap/test.g:
/// `e` = embedding dimension, `f` = Frobenius number, `m` = multiplicity,
/// `g` = genus, `c` = sigma (= 1+f−g), `sym` = IsSymmetric,
/// `apery` = AperyList w.r.t. m (indexed 0..m), `pf` = PseudoFrobenius set,
/// `t` = type.
#[allow(clippy::too_many_arguments)]
fn check(
    gens: &[usize],
    e: usize,
    f: usize,
    m: usize,
    g: usize,
    c: usize,
    sym: bool,
    apery: &[usize],
    pf: &[usize],
    t: usize,
) {
    let s = compute(gens);
    assert_eq!(s.f + s.m, s.apery_set[s.mu], "max apery");
    for i in 0..s.m {
        assert_eq!(s.diag(i) + s.apery_set[i], s.f + s.m + s.r, "diag {i}");
    }
    assert_eq!(s.diag(s.mu), s.r);
    assert_eq!(s.e, e, "e     for {gens:?}");
    assert_eq!(s.f, f, "f     for {gens:?}");
    assert_eq!(s.m, m, "m     for {gens:?}");
    assert_eq!(s.g, g, "genus for {gens:?}");
    assert_eq!(s.sigma, c, "c     for {gens:?}");
    assert_eq!(s.is_symmetric, sym, "sym   for {gens:?}");
    assert_eq!(s.apery_set.as_slice(), apery, "apery for {gens:?}");
    let mut actual_pf = s.pf_set.clone();
    actual_pf.sort_unstable();
    let mut expected_pf = pf.to_vec();
    expected_pf.sort_unstable();
    assert_eq!(actual_pf, expected_pf, "pf    for {gens:?}");
    assert_eq!(s.t, t, "type  for {gens:?}");
    assert_eq!(f + m, *apery.iter().max().unwrap(), "f ist max apery -m");
    assert_eq!(s.sigma + s.r, s.g, "g ist sigma + r");
    assert_eq!(s.f + s.r + 2, 2 * s.g + 1, "f+r+2=2g+1");
    assert_eq!(
        s.ae,
        *s.gen_set.iter().max().unwrap(),
        "maximum for {gens:?}"
    );
    assert_eq!(t, s.t, "t for {gens:?}");
    assert_eq!(t, s.pf_set.len(), "type is length pf_set");
    let mut semi_pf = s.pf_set.clone();
    semi_pf.sort_unstable();
    assert_eq!(
        pf.to_vec(),
        semi_pf,
        "pseudo-frobenius for {gens:?} and {m}"
    );
    let aps: usize = s.apery_set.iter().sum();
    assert_eq!(aps, s.apery_sum);
    // todo 82 / 85
    // rl < t whenever t > 0: every gap counted by rl is pseudo-Frobenius and
    // strictly below f, while f itself is always pseudo-Frobenius and never
    // counted by rl. So PF ⊇ {rl-counted gaps} ⊔ {f}, hence rl + 1 ≤ t.
    if s.t > 0 {
        assert!(s.rl < s.t, "rl = {}, t = {} for {gens:?}", s.rl, s.t);
    } else {
        assert_eq!(s.rl, 0);
    }
    assert!(s.t <= s.r + 1);
    assert!(s.es <= s.e);
    // todo 86: descent(ascent(S)) == S iff ascent enters its "f+m branch":
    // f+m is a min-gen AND no min-gen lies in (m, f) ∩ (f-m, f). The window
    // matches Semigroup::ascent's own filter (which excludes g = m so that
    // the trivial branch is not taken).
    let no_large_mingen = !s
        .gen_set
        .iter()
        .any(|&g| g > s.m && g < s.f && g + s.m > s.f);
    let fpm_is_mingen = s.gen_set.contains(&(s.f + s.m));
    if no_large_mingen && fpm_is_mingen {
        assert_eq!(s, s.ascent().descent(), "up-down identity for {gens:?}");
    }
    test_up_downs(&s);
    // Selmer identity: one·U(m)·c₁ = m·g + m·(m−1)/2
    let dim = m - 1;
    let u_mat = u_matrix(m);
    let cr = c_red(&s);
    let one_u: Vec<i64> = (0..dim)
        .map(|j| (0..dim).map(|a| u_mat[(a, j)]).sum())
        .collect();
    #[allow(clippy::cast_possible_wrap)]
    let c1: Vec<i64> = (0..dim).map(|i| cr[(i, 0)] as i64).collect();
    let scalar: i64 = one_u.iter().zip(c1.iter()).map(|(&u, &c)| u * c).sum();
    #[allow(clippy::cast_possible_wrap)]
    let apery_sum_selmer = (m * g + m * (m - 1) / 2) as i64;
    assert_eq!(scalar, apery_sum_selmer, "Selmer identity for {gens:?}");

    // zd(m)·c₁ = f + m + r = 2g + m − 1
    let zd = zd_vector(m);
    #[allow(clippy::cast_possible_wrap)]
    let zd_dot: i64 = (0..dim).map(|b| zd[(0, b)] * c1[b]).sum();
    #[allow(clippy::cast_possible_wrap)]
    let f_plus_m_plus_r = (f + m + s.r) as i64;
    #[allow(clippy::cast_possible_wrap)]
    let two_g_plus_m_minus_1 = (2 * g + m - 1) as i64;
    assert_eq!(zd_dot, f_plus_m_plus_r, "zd·c₁ = f+m+r for {gens:?}");
    assert_eq!(zd_dot, two_g_plus_m_minus_1, "zd·c₁ = 2g+m−1 for {gens:?}");

    // wᵢ + dᵢ = f + m + r for all i ∈ {1, …, m−1}
    let d_mat = d_matrix(m);
    for i in 1..=dim {
        #[allow(clippy::cast_possible_wrap)]
        let di: i64 = (0..dim).map(|b| d_mat[(i - 1, b)] * c1[b]).sum();
        #[allow(clippy::cast_possible_wrap)]
        let wi = s.apery_set[i] as i64;
        assert_eq!(
            wi + di,
            f_plus_m_plus_r,
            "w_{i} + d_{i} = f+m+r for {gens:?}"
        );
    }

    // level: level·m < f < (level+1)·m. f mod m = μ ≥ 1 (m ∈ S, f ∉ S),
    // so f never equals k·m and the strict inequality always holds.
    assert!(
        s.level * m < f && f < (s.level + 1) * m,
        "level invariant for {gens:?}: level={} m={} f={}",
        s.level,
        m,
        f,
    );

    // deep ⇔ every Kunz quotient q_i = (w_i − i)/m ≥ 2 (i ∈ 1..m).
    let all_q_ge_2 = (1..m).all(|i| (s.apery_set[i] - i) / m >= 2);
    assert_eq!(s.deep, all_q_ge_2, "deep ↔ q_i ≥ 2 for {gens:?}",);

    // Symmetric ⇒ r = 0 and genus = sporadics. (Every gap x has f − x ∈ S,
    // so no gap x has f − x as a gap.)
    if s.is_symmetric {
        assert_eq!(s.r, 0, "symmetric ⇒ r=0 for {gens:?}");
        assert_eq!(s.g, s.sigma, "symmetric ⇒ g = #sporadics for {gens:?}",);
    }

    // For t > 1, let L = max(PF(S) \ {f}). Then:
    //   • if f is odd, 2L ∈ S;
    //   • if f is even, 2L ∈ S or 2L = f (the boundary case where L = f/2).
    if s.t > 1 {
        let l = s
            .pf_set
            .iter()
            .copied()
            .filter(|&p| p != s.f)
            .max()
            .expect("t > 1 guarantees a pseudo-Frobenius element distinct from f");
        let two_l = 2 * l;
        if s.f.is_multiple_of(2) {
            assert!(
                s.element(two_l) || two_l == s.f,
                "2L ∈ S ∪ {{f}} where L = max(PF\\{{f}}) for {gens:?}: L={l}, 2L={two_l}, f={}",
                s.f,
            );
        } else {
            assert!(
                s.element(two_l),
                "2L ∈ S where L = max(PF\\{{f}}) for {gens:?} (f odd): L={l}, 2L={two_l}",
            );
        }
    }

    // Almost-symmetric ⇒ ra = r, PF \ {f} = reflected gaps, and f + t = 2g.
    // The symmetric case is included (t = 1, both sides empty).
    if s.is_almost_symmetric {
        assert_eq!(s.ra, s.r, "almost-symmetric ⇒ ra=r for {gens:?}");
        assert_eq!(s.f + s.t, 2 * s.g, "almost-symmetric ⇒ f+t=2g for {gens:?}",);
        let mut pf_minus_f: Vec<usize> = s.pf_set.iter().copied().filter(|&p| p != s.f).collect();
        pf_minus_f.sort_unstable();
        assert_eq!(
            pf_minus_f,
            s.blob(),
            "almost-symmetric ⇒ PF\\{{f}} = reflected gaps for {gens:?}",
        );
    }
}
/// Property checks for one-step descent / ascent.
///
/// For every non-trivial residue class `i ∈ 1..m`, looks at `w = a_i`
/// (the Apéry element with residue `i`) and runs up to four assertion
/// blocks. The four "legs" mirror the visible-arrow conditions in
/// `combined_table::arrow_cell`:
///
/// 1. **Descent of an outer Apéry** (`w > f` and `f > m`).
///    `w − m` is a gap; toggling it adds exactly one element to `S`.
///    The pair `(w − m, f − (w − m))` is a reflected-gap pair when
///    `w < f + m`, hence `r` drops by 2 and `sigma` rises by 1.
///    When `w == f + m` and `V(S) = {f−m+1, …, f−1} ⊆ S`, descent
///    collapses the conductor by `m`, raising `r` by `m − 2` and
///    raising `sigma` by `m − 1` (one new element `f` plus the
///    `m − 1` elements of `V` already in `S` reaching the new conductor).
/// 2. **Ascent of a "large" minimal generator** (`w ∈ gen_set` with
///    `f − m < w < f`). Removing `w` makes `(w, f − w)` a new
///    reflected-gap pair, so `r += 2` and `sigma -= 1`; the new
///    Apéry in residue `i` becomes `w + m`.
/// 3. **Ascent of the max Apéry** (`w == f + m ∈ gen_set`). The new
///    `a_μ` jumps by `rho · m`, where `rho = rho(S)`; the size and
///    `r` changes follow from that jump.
///
/// All four legs are exercised on every fixture in `check()`.
fn test_up_downs(s: &Semigroup) {
    if s.m == 1 {
        return;
    }
    for i in 1..s.m {
        let w = s.apery_set[i];

        // Descent: w-m is a gap; adding it lifts the membership of w-m.
        if w > s.f && s.f > s.m {
            assert!(!s.element(w - s.m));
            assert!(w <= s.f + s.m);
            assert!(
                w == s.f + s.m || s.is_reflected_gap(w - s.m),
                "reflected {w} at {i}"
            );
            let down = s.toggle(w - s.m);
            assert_eq!(down.g + 1, s.g);
            //find failed testcase assert_eq!(down.rho(),s.rho()+1);
            if w == s.f + s.m && s.v_in_s() {
                assert_eq!(down.r + 2, s.r + s.m, "r change w==f+m for i={i}");
                assert_eq!(down.sigma + s.m, s.sigma + 1, "sigma");
            }
            if w < s.f + s.m {
                assert_eq!(down.r + 2, s.r, "r change w<f+m for i={i}");
                assert_eq!(down.sigma, s.sigma + 1, "sigma");
            }
        }

        // Ascent: w must already be a minimal generator.
        if s.gen_set.contains(&w) {
            let up = s.toggle(w);
            if w < s.f && w + s.m > s.f {
                assert_eq!(up.g, s.g + 1, "removing atom generates gap for {i} and {w}");
                assert!(up.apery_set.contains(&(w + s.m)));
                assert_eq!(up.apery_set[i], w + s.m);
                assert_eq!(up.r, s.r + 2, "r change w<f for i={i}");
                assert_eq!(up.sigma + 1, s.sigma);
            }
            // Skip m == 2: {1..m-1}\{μ} = ∅, so the ρ-formula is vacuous and
            // removing the lone non-multiplicity atom collapses the semigroup
            // to ℕ via gcd-normalisation (e.g. ⟨2, 5⟩ ⇒ ⟨2⟩ ⇒ ℕ). The "ascent
            // of w_μ" structural claim assumes another residue class survives.
            if w == s.f + s.m && s.m >= 3 {
                let rho = s.rho();
                assert_eq!(
                    up.g,
                    s.g + rho,
                    "removing atom generates rho gap for {i} and {w}"
                );
                assert!(up.apery_set.contains(&(w + rho * s.m)));
                assert_eq!(up.apery_set[i], w + rho * s.m);
                assert_eq!(up.r + rho * (s.m - 2), s.r, "r change w==f+m for i={i}");
                assert_eq!(
                    up.sigma,
                    rho * (s.m - 1) + s.sigma,
                    "sigma change w==f+m for i={i}"
                );
            }
        }
    }
}

// ── CSV-driven fixtures ───────────────────────────────────────────────────
//
// Every per-semigroup `check(...)` case (originally ~108 hand-written
// `#[test] fn test_<gens>()` wrappers transcribed from gap/test.g and
// gap/newtests.g) is now sourced from `tests/data/check_fixtures.csv`.
// Columns: `gens;e;f;m;g;c;sym;apery;pf;t`, with inner arrays
// space-separated. The single `#[test] fn test_csv_fixtures` below
// drives `check()` once per row; assertion messages embed `gens:?` so a
// failure still pinpoints the offending row.

fn parse_usize_list(s: &str) -> Vec<usize> {
    s.split_ascii_whitespace()
        .map(|t| {
            t.parse::<usize>()
                .unwrap_or_else(|e| panic!("non-usize token {t:?}: {e}"))
        })
        .collect()
}

