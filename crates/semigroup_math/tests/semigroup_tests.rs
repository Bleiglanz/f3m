#[test]
fn test_is_deep() {
    // Helper: checks that `deep` matches whether all {m+1,...,2m-1} are gaps.
    let check_deep = |sg: &semigroup_math::math::Semigroup| {
        let all_gaps = (1..sg.m).all(|i| sg.is_gap(sg.m + i));
        assert_eq!(
            sg.deep, all_gaps,
            "deep disagrees with direct gap check for gens={:?} m={} f={}",
            sg.gen_set, sg.m, sg.f
        );
    };

    // <3, 7, 11>: m=3, f=8. {4, 5} are gaps (apery=[0,7,11]). deep=true.
    let sg = compute(&[3, 7, 11]);
    assert!(sg.deep);
    assert!(sg.is_gap(4) && sg.is_gap(5));
    check_deep(&sg);

    // <3, 4>: m=3, 4 ∈ S (generator). {4,5}: 4 in S → not deep.
    let sg = compute(&[3, 4]);
    assert!(!sg.deep);
    assert!(!sg.is_gap(4));
    check_deep(&sg);

    // <3, 5>: m=3, 5 ∈ S (generator). {4,5}: 5 in S → not deep.
    let sg = compute(&[3, 5]);
    assert!(!sg.deep);
    assert!(!sg.is_gap(5));
    check_deep(&sg);

    // <5, 11, 12, 13, 14>: m=5. {6,7,8,9} are all gaps (apery=[0,11,12,13,14]). deep=true.
    let sg = compute(&[5, 11, 12, 13, 14]);
    assert!(sg.deep);
    for i in 1..sg.m {
        assert!(sg.is_gap(sg.m + i), "expected gap at {}", sg.m + i);
    }
    check_deep(&sg);

    // <2, 3>: m=2. {3} is in S → not deep.
    let sg = compute(&[2, 3]);
    assert!(!sg.deep);
    assert!(!sg.is_gap(3));
    check_deep(&sg);

    // <2, 5>: m=2. {3} — 3 mod 2 = 1, apery[1]=5 > 3, so 3 is a gap. deep=true.
    let sg = compute(&[2, 5]);
    assert!(sg.deep);
    assert!(sg.is_gap(3));
    check_deep(&sg);
}

#[test]
fn test_is_descent() {
    // Checks the implication: is_descent() && max_gen == f+m  =>  deep.
    let check = |sg: &semigroup_math::math::Semigroup| {
        if sg.is_descent() && sg.max_gen == sg.f + sg.m {
            assert!(
                sg.deep,
                "is_descent && max_gen==f+m should imply deep for gens={:?}",
                sg.gen_set,
            );
        }
    };

    // <2,5>: apery=[0,5], f=3, f+m=5=max_gen. is_descent=true, deep=true.
    let sg = compute(&[2, 5]);
    assert!(sg.is_descent());
    assert_eq!(sg.max_gen, sg.f + sg.m);
    assert!(sg.deep);
    check(&sg);

    // <3,7,11>: apery=[0,7,11], f=8, f+m=11=max_gen. is_descent=true, deep=true.
    let sg = compute(&[3, 7, 11]);
    assert!(sg.is_descent());
    assert_eq!(sg.max_gen, sg.f + sg.m);
    assert!(sg.deep);
    check(&sg);

    // <3,4>: apery=[0,4,8], f=5, f+m=8. is_descent=true (4<f, 8=f+m),
    // but max_gen=4≠8=f+m and deep=false (4∈S). Implication not triggered.
    let sg = compute(&[3, 4]);
    assert!(sg.is_descent());
    assert_ne!(sg.max_gen, sg.f + sg.m);
    assert!(!sg.deep);
    check(&sg);

    // <3,4,5>: apery=[0,4,5], f=2. w_1=4>f=2 and 4≠f+m=5. is_descent=false.
    let sg = compute(&[3, 4, 5]);
    assert!(!sg.is_descent());
    check(&sg);
}

#[test]
fn test_fast_descent_drops_f_by_m_and_preserves_mu() {
    // Specification: whenever f ≥ 2m the result of fast_descent has μ unchanged
    // and Frobenius number exactly old f − m.

    // 1. Hand-checked !is_descent example: <10,11,13> has f=38, m=10, μ=8 and
    //    apéry=[0,11,22,13,24,35,26,37,48,39] — a_9 = 39 ∈ (f, f+m), so
    //    is_descent is false and naïvely adding only f would land at f=29, μ=9.
    let s = compute(&[10, 11, 13]);
    assert!(!s.is_descent());
    assert_eq!((s.f, s.m, s.mu), (38, 10, 8));
    let t = s.fast_descent();
    assert_eq!(t.f, s.f - s.m);
    assert_eq!(t.mu, s.mu);

    // 2. is_descent example for completeness: <5,7> has f=23, m=5, μ=3 and
    //    apéry=[0,21,7,28,14], so the only Apéry element ≥ f is 28 = f+m.
    let s = compute(&[5, 7]);
    assert!(s.is_descent());
    assert_eq!((s.f, s.m, s.mu), (23, 5, 3));
    let t = s.fast_descent();
    assert_eq!(t.f, s.f - s.m);
    assert_eq!(t.mu, s.mu);

    // 3. Sweep over a range of generator pairs/triples and assert the property
    //    on every semigroup with f ≥ 2m. This would catch the previous bug,
    //    which adds the Apéry elements themselves (no-ops) instead of x−m.
    let mut sweep_count = 0;
    for a in 3..=12 {
        for b in (a + 1)..=20 {
            if gcd(a, b) != 1 {
                continue;
            }
            for c in (b + 1)..=25 {
                if gcd(gcd(a, b), c) != 1 {
                    continue;
                }
                let s = compute(&[a, b, c]);
                if s.f < 2 * s.m {
                    continue;
                }
                let t = s.fast_descent();
                assert_eq!(
                    t.f,
                    s.f - s.m,
                    "fast_descent f for gens={:?}: expected {}, got {}",
                    s.gen_set,
                    s.f - s.m,
                    t.f,
                );
                assert_eq!(
                    t.mu, s.mu,
                    "fast_descent mu for gens={:?}: expected {}, got {}",
                    s.gen_set, s.mu, t.mu,
                );
                sweep_count += 1;
            }
        }
    }
    assert!(sweep_count > 0, "sweep must exercise at least one case");
}

